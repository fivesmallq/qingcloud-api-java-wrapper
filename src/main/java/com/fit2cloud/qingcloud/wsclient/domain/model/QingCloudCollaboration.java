package com.fit2cloud.qingcloud.wsclient.domain.model;

public class QingCloudCollaboration {
	private String resource_group;
	private String user_group;
	private String group_role;

	public String getResource_group() {
		return resource_group;
	}

	public void setResource_group(String resource_group) {
		this.resource_group = resource_group;
	}

	public String getUser_group() {
		return user_group;
	}

	public void setUser_group(String user_group) {
		this.user_group = user_group;
	}

	public String getGroup_role() {
		return group_role;
	}

	public void setGroup_role(String group_role) {
		this.group_role = group_role;
	}
}
